</code></pre><div class="line-numbers"><span class="line-number">1</span><br><span class="line-number">2</span><br></div></div><p><strong>悲观锁</strong></p>
<p>非观锁指对数据被外界修改持保守态度，在整个数据处理过程中，将数据处于锁定状态，可以很好地解决并发事务的更新丢失问题。</p>
<p><code>FOR UPDATE</code>，事务A执行悲观锁操作后，事务B就无法查询库存了</p>
<div class="language-sql ext-sql line-numbers-mode"><pre v-pre class="language-sql"><code><span class="token keyword">BEGIN</span><span class="token punctuation">;</span>
<span class="token keyword">SELECT</span> <span class="token operator">*</span> <span class="token keyword">FROM</span> goods <span class="token keyword">WHERE</span> id<span class="token operator">=</span><span class="token number">1</span> <span class="token keyword">FOR</span> <span class="token keyword">UPDATE</span><span class="token punctuation">;</span>
<span class="token keyword">UPDATE</span> goods <span class="token keyword">SET</span> num<span class="token operator">=</span>num<span class="token operator">-</span><span class="token number">2</span> <span class="token keyword">WHERE</span> id<span class="token operator">=</span><span class="token number">1</span><span class="token punctuation">;</span> 
</code></pre><div class="line-numbers"><span class="line-number">1</span><br><span class="line-number">2</span><br><span class="line-number">3</span><br></div></div><p><strong>乐观锁</strong></p>
<p>在每次去拿数据的时候认为别人不会修改，不对数据上锁，但是在提交更新的时候会判断在此期间数据是否被更改，如果被更改则提交失败。</p>
<p>通过设置<code>version</code>，事务A提交后<code>version</code>为1，那么事务B就更改不到数据了。</p>
<div class="language-sql ext-sql line-numbers-mode"><pre v-pre class="language-sql"><code><span class="token keyword">BEGIN</span><span class="token punctuation">;</span>
<span class="token keyword">UPDATE</span> goods <span class="token keyword">SET</span> num<span class="token operator">=</span>num<span class="token operator">-</span><span class="token number">10</span><span class="token punctuation">,</span>VERSION <span class="token operator">=</span>VERSION<span class="token operator">+</span><span class="token number">1</span> <span class="token keyword">WHERE</span> VERSION<span class="token operator">=</span><span class="token number">0</span><span class="token punctuation">;</span>
</code></pre><div class="line-numbers"><span class="line-number">1</span><br><span class="line-number">2</span><br></div></div><p><strong>读锁</strong></p>
<p>为表设置读锁后，当前会话和其他会话都不可以修改数据，但可以读取表数据。</p>
<p><code>LOCK~READ</code>：读锁</p>
<p><code>UNLOCK</code>：解除锁</p>
<div class="language-sql ext-sql line-numbers-mode"><pre v-pre class="language-sql"><code><span class="token keyword">LOCK</span> <span class="token keyword">TABLE</span> goods <span class="token keyword">READ</span><span class="token punctuation">;</span>
<span class="token keyword">UNLOCK</span> <span class="token keyword">TABLES</span><span class="token punctuation">;</span>
</code></pre><div class="line-numbers"><span class="line-number">1</span><br><span class="line-number">2</span><br></div></div><p><strong>写锁</strong></p>
<p>为表设置了写锁后，<strong>当前会话</strong>可以修改，查询表，其他会话将无法操作。</p>
<div class="language-sql ext-sql line-numbers-mode"><pre v-pre class="language-sql"><code><span class="token keyword">LOCK</span> <span class="token keyword">TABLE</span> goods <span class="token keyword">WRITE</span><span class="token punctuation">;</span>
