</code></pre><div class="line-numbers"><span class="line-number">1</span><br></div></div><p><strong>脏读</strong></p>
<p>将隔离级别设置为最低级 <code>read uncommitted</code>。</p>
<p>脏读是一个事务没有提交时可被其他事务读取到。</p>
<p>事务B在事务A没有提交时就可以看到更新的数据，如果事务A执行<code>ROLLBACK</code> 事务B的读到的数据就为<strong>脏数据</strong>。</p>
<p>将隔离级别设置为除 <code>read uncommitted</code>以外的，再重复上面的例子，都可以有效避免脏读的问题</p>
<p><strong>不可重复读</strong></p>
<p>不可重复读指在事务中多次读取的数据出现不一致的情况，我们希望读取的数据在本事务中是一致的。</p>
<ul>
<li>事务A在执行过程中更新数据，事务B同时读取的数据没有脏数据。</li>
<li>但当事务A提交了事务后，事务B再读取时得到了最新的数据，这种情况为不可重复读。</li>
<li>所以要保证事务过程中的数据重复操作是一致的，不受其他事务影响，即避免不可重复读的产生。</li>
</ul>
<p><strong>幻读</strong></p>
<p>幻读和不可重复读都是读取了另一条已经提交的事务（这点就脏读不同），所不同的是不可重复读查询的都是同一个数据项，而幻读针对的是一批数据整体。</p>
<p>事务A执行查询，假如查询结果是6条</p>
<p>事务B执行添加新数据</p>
<p>事务A执行更新，发现更新了7条（刚才查询时6条，但更新了七条，感觉像出现了幻觉）</p>
<p>换隔离级别为 <strong>SERIALIZABLE</strong> 后，在事务A没有提交时，事务B是不能插入数据的（表现形式为等待）。</p>
